            <h1>Las amenazas del lince ibérico</h1>

            <p>
                Este hermoso e icónico felino, especie bandera y símbolo de la conservación en España, presenta serias amenazas, sobre todo de origen antrópico.
                
                Entre las principales actividades responsables del declive de esta especie se encontraba la caza. El lince ibérico estuvo incluido en la infame categoría de “alimaña” desde que entró en vigor la Ley de Caza del 16 de mayo de 1902 hasta su derogación, en 1970. Dicha ley no solo permitía la eliminación del lince en cualquier época del año y con cualquier trampa o veneno, sino que incluso los Ayuntamientos animaban a hacerlo, organizaban batidas y pagaban recompensas.
                
                
                Hoy, esas mismas administraciones son las que prohíben su caza y protegen su conservación, aunque los cazadores furtivos continúan cazando ilegalmente a estos animales.</p>

           

                <img src="img/lince.jpeg" alt="">
            <p>Pero no es la caza el único riesgo para el lince ibérico. Desde la prohibición de su caza, la causa principal de mortalidad es el atropello. Cada año, varios animales mueren en nuestras carreteras. Otras amenazas importantes son los cambios en la estructura del hábitat, la expansión de las zonas urbanizadas o el cambio climático.

                Entre los riesgos biológicos, las enfermedades son también un problema emergente para las poblaciones de lince. Patologías como la mixomatosis afectan directamente a sus presas principales, los conejos. Por otro lado, la presencia de colonias de gatos ferales supone un vector de enfermedades como la leucemia felina, que provocó una epidemia en los linces de Doñana en 2007. Además, las poblaciones tan escasas, con muy pocos ejemplares en cada grupo, genera una pérdida de variabilidad genética que puede provocar la acumulación de endogamia.
                
                Dicen que los gatos tienen siete vidas. Tal vez sea cierto, pero en este caso,
                cuando a principios del siglo XX se llamó la atención sobre su situación,
                el lince ibérico ya debía de haber gastado las seis primeras.
                Miguel Delibes, “La última vida del lince ibérico”.</p>
                
